package pertemuan9;

public class orang {
	
    protected String nama;
    protected String alamat;

    public orang(String nama,String alamat) {
        this.nama = nama;
        this.alamat = alamat;
    }

    public String getNama() {
        return nama;
    }

    public String getAlamat() {
        return alamat;
    }

}
